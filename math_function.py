import math

num = 10.3
num1 = 13
num2 = 5

print(" Round function: ", round(num))
print(" Ceil function: ", math.ceil(num))
print(" Floor function: ", math.floor(num))
print(" absolute function (abs): ", abs(num))
print(" pow function: ", pow(num, 3))
print(" sqrt function: ", math.sqrt(num))
print(" biggest number : ", max(num, num1, num2))
print(" smallest number : ", min(num, num1, num2))

